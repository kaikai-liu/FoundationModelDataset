# ── Main ──────────────────────────────────────────────────────────────────
def main() -> None:
    if not DATA_CSV.exists():
        print(f'No Data.csv found at {DATA_CSV}')
        return

    print(f'Loading {DATA_CSV.relative_to(ROOT)} ...')
    df = pd.read_csv(DATA_CSV, low_memory=False)
    print(f'  shape = {df.shape}')

    acols = analytical_columns(df)
    print(f'  {len(acols)} analytical columns, {acols["element"].nunique()} unique elements')

    # Categoricals
    bar_counts(df['Deposit_Environment'].fillna('(missing)').value_counts(),
               f'Deposit_Environment (n={len(df):,})',
               OUT / 'deposit_environment_counts.png', rotation=25)
    bar_counts(df['Deposit_Group'].fillna('(missing)').value_counts(),
               f'Deposit_Group (n={len(df):,})',
               OUT / 'deposit_group_counts.png', rotation=35)
    bar_counts(df['Deposit_Type'].fillna('(missing)').value_counts().head(20),
               f'Top-20 Deposit_Type (n={len(df):,})',
               OUT / 'deposit_type_top.png', rotation=35, figsize=(12, 6))
    bar_counts(df['Country'].fillna('(missing)').value_counts().head(15),
               f'Top-15 countries (n={len(df):,})',
               OUT / 'country_counts.png', rotation=30)
    bar_counts(df['Rock_Type'].fillna('(missing)').value_counts(),
               f'Rock_Type (n={len(df):,})',
               OUT / 'rock_type_counts.png', rotation=20, color='tan')

    # Analytical structure
    plot_method_coverage(df, acols, OUT / 'method_coverage.png')
    plot_element_coverage_heatmap(df, acols, OUT / 'element_coverage_heatmap.png')
    plot_element_distributions(df, acols, OUT / 'element_distributions.png')
    plot_detection_limit_shares(df, acols, OUT / 'detection_limit_shares.png')
    plot_geographic_scatter(df, OUT / 'geographic_scatter.png')

    write_readme(df, acols, OUT / 'README.md')
    print(f'Saved previews + README to {OUT.relative_to(ROOT)}')


if __name__ == '__main__':
    main()
